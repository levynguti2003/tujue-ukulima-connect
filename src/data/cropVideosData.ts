export const getYoutubeId = (url: string) => {
  const match = url.match(
    /(?:\/|%2F|youtu\.be\/|v=|\/v\/|watch\?v=|&v=|embed\/)([A-Za-z0-9_-]{11})/
  );
  return match ? match[1] : "";
};
